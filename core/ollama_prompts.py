def build_photo_analysis_user_prompt(results: dict, profile_context: str) -> str:
    """Текст запроса к модели: англ. метки + числа + инструкция по формату ответа."""
    blocks: list[str] = []
    blocks.append(
        "Данные с модели компьютерного зрения (подписи классов; часто на английском, возможны ошибки распознавания):"
    )
    n = 0
    for p in results.get("products") or []:
        label = (p.get("label_en") or p.get("name") or "").strip()
        if not label:
            continue
        n += 1
        conf = float(p.get("confidence", 0.0))
        blocks.append(f"{n}. {label} — уверенность классификатора ~{conf * 100:.0f}%")
    if n == 0:
        blocks.append("(Список подписей классификатора пуст — опиши возможные ограничения анализа.)")

    tm = results.get("total_macros") or {}
    tc = int(results.get("total_calories", 0))
    blocks.append("")
    blocks.append(
        f"Оценка порции по внутренней таблице приложения (приблизительно): {tc} ккал; "
        f"белки ~{float(tm.get('protein', 0)):.1f} г, жиры ~{float(tm.get('fat', 0)):.1f} г, "
        f"углеводы ~{float(tm.get('carbs', 0)):.1f} г."
    )
    blocks.append("")
    blocks.append(
        "Сформируй ответ на русском в Markdown:\n"
        "## Что на фото\n"
        "Коротко перечисли блюда/продукты обычными российскими названиями (как в меню), не дословный перевод меток.\n\n"
        "## Калории и БЖУ\n"
        "Только из цифр выше — можно списком.\n\n"
        "## Советы\n"
        "2–4 практических совета по рациону. Не добавляй новых числовых оценок калорий или граммов."
    )
    if profile_context and "не заполнены" not in (profile_context or ""):
        blocks.append("")
        blocks.append(f"Контекст профиля: {profile_context[:450]}")
    return "\n".join(blocks)
